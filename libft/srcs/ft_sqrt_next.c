int		ft_sqrt_next(int nb)
{
	int	sqrt;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	sqrt = 2;
	while (sqrt * sqrt < nb)
		sqrt++;
	return (sqrt);
}
