#include <unistd.h>

void	ft_putendl(char const *s)
{
	char c;

	c = '\n';
	if (!s)
	{
		write(1, &c, 1);
		return ;
	}
	while (*s)
		write(1, s++, 1);
	write(1, &c, 1);
}
