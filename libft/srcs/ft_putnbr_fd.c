#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	if (n == -2147483648)
	{
		c = '-';
		write(fd, &c, 1);
		c = '2';
		write(fd, &c, 1);
		n = 147483648;
	}
	if (n < 0)
	{
		c = '-';
		n *= -1;
		write(fd, &c, 1);
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	c = n % 10 + '0';
	write(fd, &c, 1);
}
